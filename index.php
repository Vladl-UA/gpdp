<?php
declare(strict_types=1);

/**
 * GPDP / RNA — точка входа и инициализация системы.
 *
 * Зона ответственности ЭТОГО файла (и только она):
 *   1. Режим отладки и кодировка.
 *   2. Подключение к базе данных.
 *   3. Сбор сырых входных параметров запроса в контракт $GP.
 *   4. Передача управления конвейеру ядра (пока — заглушка, ядро
 *      находится на пересборке).
 *
 * Валидация полей, диспетчеризация по префиксам, любая бизнес-логика —
 * НЕ здесь. Это задача конвейера и обработчиков конкретных типов полей.
 */

// --- Режим отладки ----------------------------------------------------
// TODO: перенести в конфиг вместе с параметрами подключения к БД.
const GPDP_DEBUG = true;

if (GPDP_DEBUG) {
    error_reporting(E_ALL);
    ini_set('display_errors', '1');
} else {
    error_reporting(0);
    ini_set('display_errors', '0');
}

// --- Кодировка ----------------------------------------------------------
mb_internal_encoding('UTF-8');
header('Content-Type: text/html; charset=utf-8');

// --- Параметры подключения к БД ------------------------------------------
// TODO: вынести в отдельный config.php, когда появится конфигуратор.
const DB_HOST     = 'localhost';
const DB_USER     = 'root';
const DB_PASSWORD = '';
const DB_NAME     = 'stat';
const DB_CHARSET  = 'utf8mb4';

// --- Подключение к БД -----------------------------------------------------
// mysqli_report заставляет функции mysqli бросать исключения вместо
// молчаливых или полу-молчаливых сбоев, как это было с mysql_* + or die().
mysqli_report(MYSQLI_REPORT_ERROR | MYSQLI_REPORT_STRICT);

try {
    $db_connection = mysqli_connect(DB_HOST, DB_USER, DB_PASSWORD, DB_NAME);
    mysqli_set_charset($db_connection, DB_CHARSET);
} catch (mysqli_sql_exception $e) {
    // Подробности ошибки показываем только в debug-режиме: в проде это
    // риск раскрыть детали инфраструктуры (хост, версию БД и т.п.).
    if (GPDP_DEBUG) {
        die('Не удалось подключиться к базе данных: ' . $e->getMessage());
    }
    die('Сервис временно недоступен.');
}

// --- Сбор входных параметров запроса --------------------------------------
// $GP — единая точка входа сырых данных запроса. Здесь НИКАКОЙ фильтрации
// или валидации по смыслу поля не происходит: система по договорённости
// (Naming-Driven Behavior) определяет, что делать с каждым полем, по его
// префиксу — уже внутри конвейера, а не на этапе инициализации.
$GP = $_SERVER['REQUEST_METHOD'] === 'POST' ? $_POST : $_GET;

// --- Запуск конвейера -------------------------------------------------------
// TODO: ядро (диспетчер "паспорт → действие → обработчик по префиксу")
// сейчас пересобирается отдельно — см. RNA GPDP v1.0.
// Когда будет готово:
//
//     require_once 'core.php';
//     run_pipeline($GP, $db_connection);
//
// Временная проверка инициализации, чтобы видеть, что дошли до этой точки:
if (GPDP_DEBUG) {
    echo '<pre>';
    echo "Инициализация пройдена.\n";
    echo "Подключение к БД: OK (" . DB_NAME . ")\n";
    echo "Входные параметры (\$GP):\n";
    print_r($GP);
    echo '</pre>';
}
